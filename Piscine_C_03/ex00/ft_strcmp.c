int	ft_strcmp(char *s1, char *s2)
{
	int	idx;

	idx = 0;
	while (s1[idx] == s2[idx])
	{
		if (s1[idx] == '\0')
			return (0);
		idx++;
	}
	return (s1[idx] - s2[idx]);
}
